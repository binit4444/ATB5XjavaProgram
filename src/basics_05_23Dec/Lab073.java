public class Lab073 {
    public static void main(String[] args) {

        // for(A,B,C)
        // A -> Initialization -> JVM what variable, where you are starting point.
        // B -> Condition -> When you want to stop this.
        // C -> Increment/ Decrement


        for (int i = 1; i <=10000; i++) {
            System.out.println("Name");
        }

        // Exit code 0 -> Normal Execution
        // Exit code 130-137 or any number  -> User Interrupted
        // Means not a normal programming

        System.out.println(" --- End of Program ---");

    }
}
