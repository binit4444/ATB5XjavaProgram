package src.basics_02;

public class Lab017 {

    public static void main(String[] args) {
        byte a = 10;
        byte b = 18;
        System.out.println(a+b);
        // a is integer when you reuse in the print tunnel

        // 1st operand type  | 2nd operand type  | Result Type
        // byte               +  byte            =  int
        // char               +  char            =   int
        // int                +  byte            =   int
        // int                +  long            =   long
        // so bigger will take the result value.
    }
}
